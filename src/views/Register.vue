<template>
    <div>
        <van-nav-bar
            title="用户注册"
            left-text="返回"
            left-arrow
            @click-left = "goBack"
        />

        <div class="register-panel">
            <van-field
                v-model="username"
                label="用户名"
                clearable
                placeholder="请输入用户名"
                required
                
            />
            <van-field
                v-model="password"
                type="password"
                label="密码"
                placeholder="请输入用户密码"
                required
            />
            <div class="register-bunnton">
                <van-button type="primary" @click="axiosRegisterUser" size="large">注册</van-button>
            </div>
        </div>

    </div>
</template>

<script>
    import axios  from 'axios'
    import url from '@/serviceApi.config.js'
    export default {
        data() {
            return {
                username: "",
                password: ""
            }
        },
        methods: {
            goBack() {
                this.$router.go(-1)
            },
            axiosRegisterUser(){
                axios({
                    url:url.resisterUser,
                    method:"post",
                    data:{
                        username:this.username,
                        password:this.password
                    }

                })
                .then(response=>{
                    console.log(response)
                })
                .catch((error)=>{
                    console.log(error)
                })
            }
        },
    }
</script>

